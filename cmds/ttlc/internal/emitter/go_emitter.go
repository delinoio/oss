package emitter

import (
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"runtime"
	"sort"
	"strings"

	"github.com/delinoio/oss/cmds/ttlc/internal/sema"
)

type EmitResult struct {
	Path    string
	Content []byte
}

func EmitGo(moduleName string, types []sema.TypeDecl, tasks []sema.Task, outDir string) (EmitResult, error) {
	if strings.TrimSpace(moduleName) == "" {
		return EmitResult{}, fmt.Errorf("module name is required")
	}
	if strings.TrimSpace(outDir) == "" {
		return EmitResult{}, fmt.Errorf("out-dir is required")
	}
	if err := os.MkdirAll(outDir, 0o700); err != nil {
		return EmitResult{}, fmt.Errorf("create out-dir: %w", err)
	}
	if err := chmodDirectory(outDir); err != nil {
		return EmitResult{}, err
	}

	sortedTasks := append([]sema.Task{}, tasks...)
	sort.Slice(sortedTasks, func(left int, right int) bool {
		return sortedTasks[left].ID < sortedTasks[right].ID
	})
	sortedTypes := append([]sema.TypeDecl{}, types...)
	sort.Slice(sortedTypes, func(left int, right int) bool {
		return sortedTypes[left].Name < sortedTypes[right].Name
	})

	builder := strings.Builder{}
	builder.WriteString("// Code generated by ttlc. DO NOT EDIT.\n")
	builder.WriteString("package ")
	builder.WriteString(moduleName)
	builder.WriteString("\n\n")
	builder.WriteString("import \"context\"\n\n")
	builder.WriteString("type Vc[T any] struct{ value T }\n\n")
	builder.WriteString("var _ = context.Background\n\n")
	for _, typeDeclaration := range sortedTypes {
		builder.WriteString("type ")
		builder.WriteString(typeDeclaration.Name)
		builder.WriteString(" struct {\n")
		for _, field := range typeDeclaration.Fields {
			builder.WriteString("\t")
			builder.WriteString(field.Name)
			builder.WriteString(" ")
			builder.WriteString(field.Type)
			builder.WriteString("\n")
		}
		builder.WriteString("}\n\n")
	}
	for _, task := range sortedTasks {
		builder.WriteString("func ")
		builder.WriteString(task.ID)
		builder.WriteString("(")
		for index, parameter := range task.Params {
			if index > 0 {
				builder.WriteString(", ")
			}
			builder.WriteString(parameter.Name)
			builder.WriteString(" ")
			builder.WriteString(parameter.Type)
		}
		builder.WriteString(") ")
		builder.WriteString(task.ReturnType)
		builder.WriteString(" {\n")
		for _, parameter := range task.Params {
			builder.WriteString("\t_ = ")
			builder.WriteString(parameter.Name)
			builder.WriteString("\n")
		}
		builder.WriteString("\t// TODO(ttlc): wire runtime.Task invocation in phase 2.\n")
		builder.WriteString("\treturn ")
		builder.WriteString(zeroValueForType(task.ReturnType))
		builder.WriteString("\n")
		builder.WriteString("}\n\n")
	}

	formatted, err := format.Source([]byte(builder.String()))
	if err != nil {
		return EmitResult{}, fmt.Errorf("format generated go source: %w", err)
	}

	filePath := filepath.Join(outDir, sanitizeModuleName(moduleName)+"_ttl_gen.go")
	if err := os.WriteFile(filePath, formatted, 0o600); err != nil {
		return EmitResult{}, fmt.Errorf("write generated go file: %w", err)
	}
	if err := chmodFile(filePath); err != nil {
		return EmitResult{}, err
	}

	return EmitResult{Path: filePath, Content: formatted}, nil
}

func sanitizeModuleName(value string) string {
	if value == "" {
		return "module"
	}
	builder := strings.Builder{}
	for _, runeValue := range value {
		if (runeValue >= 'a' && runeValue <= 'z') || (runeValue >= 'A' && runeValue <= 'Z') || (runeValue >= '0' && runeValue <= '9') {
			builder.WriteRune(runeValue)
			continue
		}
		builder.WriteRune('_')
	}
	result := builder.String()
	if result == "" {
		return "module"
	}
	return result
}

func zeroValueForType(typeName string) string {
	trimmed := strings.TrimSpace(typeName)
	if trimmed == "" {
		return ""
	}
	if strings.Contains(trimmed, "[") || strings.Contains(trimmed, ".") || strings.HasPrefix(trimmed, "Vc") {
		return trimmed + "{}"
	}
	switch trimmed {
	case "string":
		return "\"\""
	case "bool":
		return "false"
	case "int", "int8", "int16", "int32", "int64", "uint", "uint8", "uint16", "uint32", "uint64", "uintptr", "float32", "float64":
		return "0"
	default:
		return trimmed + "{}"
	}
}

func chmodDirectory(path string) error {
	if runtime.GOOS == "windows" {
		return nil
	}
	if err := os.Chmod(path, 0o700); err != nil {
		return fmt.Errorf("chmod out-dir: %w", err)
	}
	return nil
}

func chmodFile(path string) error {
	if runtime.GOOS == "windows" {
		return nil
	}
	if err := os.Chmod(path, 0o600); err != nil {
		return fmt.Errorf("chmod generated file: %w", err)
	}
	return nil
}
